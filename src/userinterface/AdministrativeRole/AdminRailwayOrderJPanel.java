    public void populateTable(){
        
        int rowCount = tableOrderHistory.getRowCount();
        DefaultTableModel model = (DefaultTableModel)tableOrderHistory.getModel();
        for(int i=rowCount-1;i>=0;i--) {
            model.removeRow(i);
        }
        //System.out.println(customerOrganization.getUserAccountDirectory().getUserAccountList().size());
        for(UserAccount cusAccount : customerOrganization.getUserAccountDirectory().getUserAccountList()){
            if (cusAccount.getCustomerRailWayBooking().size() == 0){
                continue;
            }
            int cnt = 0;
            //System.out.println("Flight orders size: "+cusAccount.getFlightOrder().size());
            for (RailWay r : cusAccount.getCustomerRailWayBooking()){
                Object row[] = new Object[8];
                row[0] = cusAccount.getEmail();
                row[1] = cusAccount.getCustomerRailWayBooking().get(cnt).getCompanyName();
                row[2] = cusAccount.getCustomerRailWayBooking().get(cnt).getRailwayNum();
                row[3] = cusAccount.getCustomerRailWayBooking().get(cnt).getDate();
                row[4] = cusAccount.getCustomerRailWayBooking().get(cnt).getDeparture();
                row[5] = cusAccount.getCustomerRailWayBooking().get(cnt).getArrival();
                row[6] = cusAccount.getCustomerRailWayBooking().get(cnt).getSeatType();
                if (cusAccount.getWorkQueue().getRequestMap().get("RailWay").get(cnt).getPriceChange()){
                    row[7] = cusAccount.getWorkQueue().getRequestMap().get("RailWay").get(cnt).getPrice();
                }
                else{
                    row[7] = r.getPrice();
                }
                model.addRow(row);
                cnt++;
            }
        }
        
    }
    
