    private void btnAnalyzeRailwayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnalyzeRailwayActionPerformed
        // TODO add your handling code here:
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        for(UserAccount cusAccount : customerOrganization.getUserAccountDirectory().getUserAccountList()){
            if (cusAccount.getCustomerRailWayBooking().size() == 0){
                continue;
            }
            for (RailWay r : cusAccount.getCustomerRailWayBooking()){
                if (railwayMap.containsKey(r.getCompanyName())){

                    railwayMap.put(r.getCompanyName(), railwayMap.get(r.getCompanyName())+r.getPrice());
                }
                else{

                    railwayMap.put(r.getCompanyName(), r.getPrice());

                }
            }
        }
        Set<Map.Entry<String, Double>> set = railwayMap.entrySet();
        List<Map.Entry<String, Double>> list = new ArrayList<Map.Entry<String, Double>>(set);
        Collections.sort( list, new Comparator<Map.Entry<String, Double>>()
            {
                public int compare( Map.Entry<String, Double> o1, Map.Entry<String, Double> o2 )
                {
                    return (o2.getValue()).compareTo( o1.getValue() );
                }
            } );
            for(Map.Entry<String, Double> entry:list){
                dataset.setValue(entry.getValue(), "Sales Value($)", entry.getKey());
            }
            JFreeChart railwayChart = ChartFactory.createBarChart("Railway Total Sales Value", "Railway Name", "Sales Value($)", dataset, PlotOrientation.VERTICAL, false, true, false);
            CategoryPlot p = railwayChart.getCategoryPlot();
            p.setRangeGridlinePaint(Color.BLACK);
            ChartFrame frame = new ChartFrame("Bar chart for railways", railwayChart);
            frame.setVisible(true);
            frame.setSize(800, 500);
    }//GEN-LAST:event_btnAnalyzeRailwayActionPerformed


