    public CustomerAirFlightJPanel(JPanel userProcessContainer, UserAccount account, Organization organization, EcoSystem business) {
        initComponents();
        this.userProcessContainer = userProcessContainer;
        this.userAccount = account;
        this.system = business;
        this.customerOrganization = (CustomerOrganization)organization;
        comboFlightNum.removeAllItems();
        comboDeparture.removeAllItems();
        comboArrival.removeAllItems();
        comboDate.removeAllItems();
        comboTime.removeAllItems();
        
        comboFlightNum.addItem("");
        comboDeparture.addItem("");
        comboArrival.addItem("");
        comboDate.addItem("");
        comboTime.addItem("");
        ArrayList<Object> tempDep = new ArrayList<Object>();
        ArrayList<Object> tempArr = new ArrayList<Object>();
        ArrayList<Object> tempDate = new ArrayList<Object>();
        ArrayList<Object> tempTime = new ArrayList<Object>();
        for (Network network : this.system.getNetworkList()){
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
                if (enterprise.getName().equals(customerOrganization.getEnterpriseName())){
                    //System.out.println(enterprise.getName());
                    for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()){
                        if (org.getName().equals("Airliner Organization")){
                            for (UserAccount useraccount : org.getUserAccountDirectory().getUserAccountList()){
                                for (Flight flight : useraccount.getFlightCatalog()){
                                    comboFlightNum.addItem(flight.getFlightNum());
                                    if (!tempDep.contains(flight.getDeparture())){
                                        comboDeparture.addItem(flight.getDeparture());
                                        tempDep.add(flight.getDeparture());
                                    }
                                    if (!tempArr.contains(flight.getArrival())){
                                        comboArrival.addItem(flight.getArrival());
                                        tempArr.add(flight.getArrival());
                                    }
                                    if (!tempDate.contains(flight.getDate())){
                                        comboDate.addItem(flight.getDate());
                                        tempDate.add(flight.getDate());
                                    }
                                    if (tempTime.contains(flight.getTime())){
                                        comboTime.addItem(flight.getTime());
                                        tempTime.add(flight.getTime());
                                    }
                                }
                            }
                        }
                    }
                }
            }
        }
        
    }

