    private void btnContinueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinueActionPerformed
        // TODO add your handling code here:
        String flightNum = (String)comboFlightNum.getSelectedItem();
        String departure = (String)comboDeparture.getSelectedItem();
        String arrival = (String)comboArrival.getSelectedItem();
        String time = (String)comboTime.getSelectedItem();
        String date = (String)comboDate.getSelectedItem();
        if (searchAir(flightNum, departure, arrival, time, date) == null){
            JOptionPane.showMessageDialog(null, "No available Car found:( Search again!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        ArrayList<Flight> flightList = new ArrayList<Flight>();
        flightList = searchAir(flightNum, departure, arrival, time, date);
        CustomerFlightResultJPanel cusCarSearchResJP = new CustomerFlightResultJPanel(userProcessContainer, userAccount, customerOrganization, system, flightList);
        userProcessContainer.add("CustomerCarSearchResultJPanel",cusCarSearchResJP);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnContinueActionPerformed

    private ArrayList<Flight> searchAir(String flightNum, String departure, String arrival, String time, String date){
        ArrayList<Flight> flightList = new ArrayList<Flight>();
        int cnt = 0;
        for (Network network : system.getNetworkList()){
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
                //System.out.println("Customer's enterprise: " + customerOrganization.getEnterpriseName() + ", Enterprise in directory: " + enterprise.getEnterpriseName());
                if (enterprise.getName().equals(customerOrganization.getEnterpriseName())){
                   for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()){
                        if (org.getName().equals("Airliner Organization")){
                            for (UserAccount useraccount : org.getUserAccountDirectory().getUserAccountList()){
                                for (Flight f : useraccount.getFlightCatalog()){
                                    if ((!flightNum.equals("")) && (f.getFlightNum().equals(flightNum) == false)){
                                        continue;
                                    }
                                    if ((!departure.equals("")) && (f.getDeparture().equals(departure)==false)){
                                        continue;
                                    }
                                    if ((!arrival.equals("")) && (f.getArrival().equals(arrival)==false)){
                                        continue;
                                    }
                                    if ((!time.equals("")) && (f.getTime().equals(time)==false)){
                                        continue;
                                    }
                                    if ((!date.equals("")) && (f.getDate().equals(date)==false)){
                                        continue;
                                    }
                                    flightList.add(f);
                                    cnt++;
                                }
                            }
                        }
                    } 
                }
                
            }
        }
        if (cnt == 0){
            return null;
        }
        return flightList;
    }
    
