    private void btnRemoveFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveFlightActionPerformed
        int row = tableCars.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Car myCar = (Car)tableCars.getValueAt(row, 0);
        if (!myCar.getIsAvailable()){
            JOptionPane.showMessageDialog(null, "This car is already reserved, you can't delete this car just like that!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        userAccount.getCarCatalog().remove(myCar);
        
        populateTable();
    }//GEN-LAST:event_btnRemoveFlightActionPerformed

