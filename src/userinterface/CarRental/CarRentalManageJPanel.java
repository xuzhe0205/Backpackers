    public void populateTable(){
        int rowCount = tableCars.getRowCount();
        DefaultTableModel model = (DefaultTableModel)tableCars.getModel();
        for(int i=rowCount-1;i>=0;i--) {
            model.removeRow(i);
        }
        for(Car myCar : userAccount.getCarCatalog()) {
            Object row[] = new Object[4];
            row[0] = myCar;
            row[1] = myCar.getMade();
            row[2] = myCar.getModel();
            row[3] = myCar.getLocation();
            model.addRow(row);
        }
    }
    
    
