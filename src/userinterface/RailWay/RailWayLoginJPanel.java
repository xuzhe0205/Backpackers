    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // Get user name
        String userName = txtUserName.getText();
        // Get Password
        char[] passwordCharArray = txtPassword.getPassword();
        String password = String.valueOf(passwordCharArray);
        String enterpriseType;

        if (radioIndustry.isSelected()){
            enterpriseType = "Industrial Travel Agency";
        }
        else{
            enterpriseType = "Neighborhood Travel Agency";
        }
        //Step1: Check in the system admin user account directory if you have the user
        UserAccount userAccount=null;

        Enterprise inEnterprise=null;
        Organization inOrganization=null;

        //Step 2: Go inside each network and check each enterprise
        for(Network network:system.getNetworkList()){
            //Step 2.a: check against each enterprise
            for(Enterprise enterprise:network.getEnterpriseDirectory().getEnterpriseList()){
                //userAccount=enterprise.getUserAccountDirectory().authenticateUser(userName, password);
                if(userAccount==null){
                    //Step 3:check against each organization for each enterprise
                    for(Organization organization:enterprise.getOrganizationDirectory().getOrganizationList()){
                        if (organization.getName().equals("RailWay Organization")){
                            userAccount=organization.getUserAccountDirectory().authenticateUser(userName, password);
                        }

                        if(userAccount!=null){
                            inEnterprise=enterprise;
                            inOrganization=organization;
                            break;
                        }
                    }

                }
                else{
                    inEnterprise=enterprise;
                    break;
                }
                if(inOrganization!=null){
                    break;
                }
            }
            if(inEnterprise!=null){
                break;
            }
        }

        if(userAccount==null){
            JOptionPane.showMessageDialog(null, "Invalid credentials");
            return;
        }
        else{
            RailWayDirectionJPanel railwayDirectionJPanel = new RailWayDirectionJPanel(userProcessContainer, userAccount, inOrganization, system);
            userProcessContainer.add("RailWayDpirectionJPanel",railwayDirectionJPanel);
            CardLayout layout=(CardLayout)userProcessContainer.getLayout();
            //userAccount.getRole().createWorkArea(userProcessContainer, userAccount, inOrganization, inEnterprise, system);

            layout.next(userProcessContainer);
            //            ManageAirlinerJPanel maAirJPanel = new ManageAirlinerJPanel(userProcessContainer, airlinerDir);
            //        userProcessContainer.add("ManageAirlinerJpanel", maAirJPanel);
            //        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            //        layout.next(userProcessContainer);
        }

        btnLogin.setEnabled(false);
        txtUserName.setEnabled(false);
        txtPassword.setEnabled(false);
        logoutJButton2.setEnabled(true);
    }//GEN-LAST:event_btnLoginActionPerformed

