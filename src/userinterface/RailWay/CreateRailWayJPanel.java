    private void addRailWayBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addRailWayBtnActionPerformed
        // TODO add your handling code here:

        String railwayNum;
      
        String date;
        String departure;
        String arrival;
        double price;
        double roomAvail;
        if (txtRailWayNum.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Error: You must enter a valid string for Flight Number", "Warning", JOptionPane.INFORMATION_MESSAGE);
            txtRailWayNum.setText("");
            return;
        }
        else{
            railwayNum = txtRailWayNum.getText();
        }

        

        if (txtDate.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Error: You must enter a valid string for Date!", "Warning", JOptionPane.INFORMATION_MESSAGE);
            txtDate.setText("");
            return;
        }
        else{
            date = txtDate.getText();
        }

        if (txtDeparture.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Error: You must enter a valid string for Departure!", "Warning", JOptionPane.INFORMATION_MESSAGE);
            txtDeparture.setText("");
            return;
        }
        else{
            departure = txtDeparture.getText();
        }

        if (txtArrival.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Error: You must enter a valid string for Arrival!", "Warning", JOptionPane.INFORMATION_MESSAGE);
            txtArrival.setText("");
            return;
        }
        else{
            arrival = txtArrival.getText();
        }

        try{
            price = Double.parseDouble(txtPrice.getText());
        }
        catch(InputMismatchException exception){
            JOptionPane.showMessageDialog(null, "Error: You must enter a double for the Price!", "Warning", JOptionPane.INFORMATION_MESSAGE);
            txtPrice.setText("");
            return;
        }
        
        try{
            roomAvail = Double.parseDouble(txtRoomAvail.getText());
        }
        catch(InputMismatchException exception){
            JOptionPane.showMessageDialog(null, "Error: You must enter a double for the Available Rooms!", "Warning", JOptionPane.INFORMATION_MESSAGE);
            txtRoomAvail.setText("");
            return;
        }
        
//        for (RailWay r : railwayDir.getRailwayDir()){
//            if (r.getRailwayNum().equals(flightNum)){
//                JOptionPane.showMessageDialog(null, "Error: This FLight Number already exists, it should be unique! Try again!", "Warning", JOptionPane.INFORMATION_MESSAGE);
//                txtRailWayNum.setText("");
//                return;
//            }
//        }

        RailWay railway = useraccount.addRailway();
        railway.setRailwayNum(railwayNum);
        
        railway.setDate(date);
        railway.setTime((String)comboTime.getSelectedItem());
        railway.setSeatType((String)comboSeatType.getSelectedItem());
        railway.setDeparture(departure);
        railway.setArrival(arrival);
        railway.setSeatAvail(300);
        railway.setPrice(price);
        railway.setCompanyName(useraccount.getEmployee().getName());
        railway.setRoomAvail(roomAvail);
        railway.setRoomType((String)comboRoom.getSelectedItem());
        //myFlight.setAirlinerName(myAirliner.getAirlinerName());
        JOptionPane.showMessageDialog(null, "RailWay successfully added", "Warning", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_addRailWayBtnActionPerformed

