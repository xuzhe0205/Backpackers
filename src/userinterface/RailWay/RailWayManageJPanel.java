    private void deleterailwayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleterailwayActionPerformed
        // TODO add your handling code here:
         int selectedrow = viewRailWayTable.getSelectedRow();
        
        if(selectedrow>=0){
            int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete??","Warning",selectionButton);
            if(selectionResult == JOptionPane.YES_OPTION){
                RailWay railway = (RailWay)viewRailWayTable.getValueAt(selectedrow, 0);
                userAccount.delRailway(railway);
                populateTable();
        }
        }
        else 
            JOptionPane.showMessageDialog(null,"Please select any row");
    }//GEN-LAST:event_deleterailwayActionPerformed

    private void viewrailwayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewrailwayActionPerformed
        // TODO add your handling code here:
        String railwayid;
        if (txtSearchid.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Error: You must enter a valid string for Car ID", "Warning", JOptionPane.INFORMATION_MESSAGE);
            txtSearchid.setText("");
            return;
        }
        else{
            railwayid = txtSearchid.getText();
        }
        for (RailWay railway : userAccount.getRailwayCatalog()){
            if (Integer.toString(railway.getRailwayid()).equals(railwayid)){
                txtSearchid.setText("");
                ViewRailWayJPanel viewRailWayJPanel = new ViewRailWayJPanel(userProcessContainer, railway);
                userProcessContainer.add("ViewRailWayJPanel", viewRailWayJPanel);
                CardLayout layout = (CardLayout)userProcessContainer.getLayout();
                layout.next(userProcessContainer);
                return;
            }
        }
        JOptionPane.showMessageDialog(null, "No such Hotel is found :(", "Warning", JOptionPane.INFORMATION_MESSAGE);
        txtSearchid.setText("");
    }//GEN-LAST:event_viewrailwayActionPerformed

