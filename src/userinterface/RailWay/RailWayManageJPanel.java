    public void populateTable(){
        int rowCount = viewRailWayTable.getRowCount();
        DefaultTableModel model = (DefaultTableModel)viewRailWayTable.getModel();
        for(int i=rowCount-1;i>=0;i--) {
            model.removeRow(i);
        }
        for(RailWay railway : userAccount.getRailwayCatalog()) {
            Object row[] = new Object[4];
            row[0] = railway;
            row[1] = railway.getRailwayNum();
            row[2] = railway.getDeparture();
            row[3] = railway.getArrival();
            model.addRow(row);
        }
    }
