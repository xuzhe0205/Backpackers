    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        for (Airliner a : airlinerDir.getAirlinerDir()){
            if (txtAirliner.getText().equals(a.getAirlinerName())){
                JOptionPane.showMessageDialog(null, "This airliner already exists:( Register another one with a different name!", "Warning", JOptionPane.INFORMATION_MESSAGE);
                txtAirliner.setText("");
                return;
            }
        }
        for (Airliner a : airlinerDir.getAirlinerDir()){
            if (a.getAirlinerName().equals(myAirliner.getAirlinerName())){
                a.setAirlinerName(txtAirliner.getText());
            }
        }

        JOptionPane.showMessageDialog(null, "Airliner successfully updated", "Warning", JOptionPane.INFORMATION_MESSAGE);
        
    }//GEN-LAST:event_btnUpdateActionPerformed


