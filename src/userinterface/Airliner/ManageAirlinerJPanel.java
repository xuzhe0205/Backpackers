    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String flightNum;
        if (txtFlightNum.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Error: You must enter a valid string for Car ID", "Warning", JOptionPane.INFORMATION_MESSAGE);
            txtFlightNum.setText("");
            return;
        }
        else{
            flightNum = txtFlightNum.getText();
        }
        for (Flight f : userAccount.getFlightCatalog()){
            if (f.getFlightNum().equals(flightNum)){
                txtFlightNum.setText("");
                ViewFlightJPanel viewFlightJPanel = new ViewFlightJPanel(userProcessContainer, userAccount, f);
                userProcessContainer.add("ViewFlightDetailJPanel", viewFlightJPanel);
                CardLayout layout = (CardLayout)userProcessContainer.getLayout();
                layout.next(userProcessContainer);
                return;
            }
        }
        JOptionPane.showMessageDialog(null, "No such Car is found :(", "Warning", JOptionPane.INFORMATION_MESSAGE);
        txtFlightNum.setText("");
        return;
    }//GEN-LAST:event_btnSearchActionPerformed

