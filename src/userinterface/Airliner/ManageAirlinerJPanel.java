    private void btnRemoveFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveFlightActionPerformed
        // TODO add your handling code here:
        int row = viewFlightTable.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Flight myFlight = (Flight)viewFlightTable.getValueAt(row, 0);
        UserAccount[][] seats = myFlight.getFlightSeats();
        int cnt = 0;
        for(int i = 0; i < 25; i++){
            for (int j = 0; j < 6; j++){
                if (seats[i][j]!=null){
                    cnt++;
                    JOptionPane.showMessageDialog(null, "There are orders processed in flight, you can't delete this flight just like that!", "Warning", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }
        }
        if (cnt==0){
            userAccount.getFlightCatalog().remove(myFlight);
        }
        
        refreshTable();
    }//GEN-LAST:event_btnRemoveFlightActionPerformed

