    public void refreshTable() {
        int rowCount = viewFlightTable.getRowCount();
        DefaultTableModel model = (DefaultTableModel)viewFlightTable.getModel();
        for(int i=rowCount-1;i>=0;i--) {
            model.removeRow(i);
        }
        for(Flight myFlight : userAccount.getFlightCatalog()) {
            Object row[] = new Object[3];
            row[0] = myFlight;
            row[1] = myFlight.getDeparture();
            row[2] = myFlight.getArrival();
            model.addRow(row);
        }
    }
    
