    private void btnAddHotelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddHotelActionPerformed
        // TODO add your handling code here:
        //String name;
       
        double roomnumbers;
        String location;
        double price;
        double meetingroomnumber;
        //String roomtype;
//        if (txtName.getText().trim().isEmpty()){
//            JOptionPane.showMessageDialog(null, "Error: You must enter a valid string for Hotel Name", "Warning", JOptionPane.INFORMATION_MESSAGE);
//            txtPrice.setText("");
//            return;
//        }
//        else{
//            name = txtName.getText();
//        }

        if (txtLocation.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Error: You must enter a valid string for Hotel Name", "Warning", JOptionPane.INFORMATION_MESSAGE);
            txtLocation.setText("");
            return;
        }
        else{
            location = txtLocation.getText();
        }

        

        try{
            price = Double.parseDouble(txtPrice.getText());
        }
        catch(NumberFormatException exception){
            JOptionPane.showMessageDialog(null, "Error: You must enter a double for the Price!", "Warning", JOptionPane.INFORMATION_MESSAGE);
            txtPrice.setText("");
            return;
        }
        
        try{
            roomnumbers = Double.parseDouble(txtRoomNumbers.getText());
        }
        catch(NumberFormatException exception){
            JOptionPane.showMessageDialog(null, "Error: You must enter a double for the Room Numbers!", "Warning", JOptionPane.INFORMATION_MESSAGE);
            txtRoomNumbers.setText("");
            return;
        }
        
        try{
            meetingroomnumber = Double.parseDouble(txtMeetingRoomNumbers2.getText());
        }
        catch(NumberFormatException exception){
            JOptionPane.showMessageDialog(null, "Error: You must enter a double for the Room Numbers!", "Warning", JOptionPane.INFORMATION_MESSAGE);
            txtMeetingRoomNumbers2.setText("");
            return;
        }
        
        
        String stars = (String)comboStars.getSelectedItem();
        String parking = (String)comboParking.getSelectedItem();
        String wifi = (String)comboWifi.getSelectedItem();
        String roomtype = (String)comboRoomtype.getSelectedItem();
        String meetingroomtype = (String)comboMeetingRoomtype.getSelectedItem();
        Hotel hotel = useraccount.addHotel();
        //hotel.setName(name);
        hotel.setLocation(location);
        hotel.setPrice(price);
        hotel.setRoomnumbers(roomnumbers);
        hotel.setStars(stars);
        hotel.setParking(parking);
        hotel.setName(useraccount.getEmployee().getName());
        hotel.setWifi(wifi);
        hotel.setRoomtype(roomtype);
        hotel.setMeetingroomnumbers(meetingroomnumber);
        hotel.setMeetingroomtype(meetingroomtype);
        //hotel.setHotelid(hotel.getHotelid());
        JOptionPane.showMessageDialog(null, "Hotel successfully added", "Warning", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnAddHotelActionPerformed

