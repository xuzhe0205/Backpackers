     public void populateTable(){
        int rowCount = hotelTable.getRowCount();
        DefaultTableModel model = (DefaultTableModel)hotelTable.getModel();
        for(int i=rowCount-1;i>=0;i--) {
            model.removeRow(i);
        }
        
        for(Hotel hotel : userAccount.getHotelCatalog()) {
            Object row[] = new Object[4];
            row[0] = hotel;
            row[1] = hotel.getName();
            row[2] = hotel.getStars();
            row[3] = hotel.getLocation();
            model.addRow(row);
        }
    }

