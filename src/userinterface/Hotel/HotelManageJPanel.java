    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String hotelid;
        if (txtSearchid.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Error: You must enter a valid string for Car ID", "Warning", JOptionPane.INFORMATION_MESSAGE);
            txtSearchid.setText("");
            return;
        }
        else{
            hotelid = txtSearchid.getText();
        }
        for (Hotel hotel : userAccount.getHotelCatalog()){
            if (Integer.toString(hotel.getHotelid()).equals(hotelid)){
                txtSearchid.setText("");
                ViewHotelJPanel viewHotelJPanel = new ViewHotelJPanel(userProcessContainer, hotel);
                userProcessContainer.add("ViewHotelJPanel", viewHotelJPanel);
                CardLayout layout = (CardLayout)userProcessContainer.getLayout();
                layout.next(userProcessContainer);
                return;
            }
        }
        JOptionPane.showMessageDialog(null, "No such Hotel is found :(", "Warning", JOptionPane.INFORMATION_MESSAGE);
        txtSearchid.setText("");
        
    }//GEN-LAST:event_btnSearchActionPerformed

